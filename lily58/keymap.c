bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
	case QWERTY:
	  if (record->event.pressed) {
		default_layer_set(1UL<<_QWERTY);
	  }
	  return false;
	case COLEMAK:
	  if (record->event.pressed) {
		default_layer_set(1UL<<_COLEMAK);
	  }
	  return false;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
		update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
		update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
		update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
		update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
	}
  return true;
}

